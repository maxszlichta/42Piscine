int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	if (nb > 0)
	{
		while (n * n <= nb)
		{
			if (n * n == nb)
				return (n);
			if (n > 46340)
				return (0);
			++n;
		}
	}
	return (0);
}
